import UIKit


public extension UIColor {
    
    /// GitCoMing - DarkGray Color
    class var gitDarkGray: UIColor? { return UIColor(named: "gitDarkGray") }
    
    /// GitCoMing - Purple Color
    class var gitPurple: UIColor? { return UIColor(named: "gitPurple") }
    
    /// GitCoMing - Black Color
    class var gitBlack: UIColor? { return UIColor(named: "gitBlack")}
    
    /// GitCoMing - White Color
    class var gitWhite: UIColor? { return UIColor(named: "gitWhite")}
    
    /// GitCoMing - Blue Color
    class var gitBlue: UIColor? { return UIColor(named: "gitBlue")}
    
    /// GitCoMing - Light Color
    class var gitLightGray: UIColor? { return UIColor(named: "gitLightGray")}
    
    /// GitCoMing - Gray Color
    class var gitGray: UIColor? { return UIColor(named: "gitGray") }
    
    /// GitCoMing - SilverGray Color
    class var gitSilverGray: UIColor? { return UIColor(named: "gitSilverGray")}
    
    /// GitCoMing - ClearGray Color
    class var gitClearGray: UIColor? { return UIColor(named: "gitClearGray") }
    
    /// GitCoMing - DimGray Color
    class var gitDimGray: UIColor? { return UIColor(named: "gitDimGray") }
}



